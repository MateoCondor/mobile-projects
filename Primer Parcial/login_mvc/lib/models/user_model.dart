class UserModel {
  String username;
  String password;
  String firstName;
  String lastName;
  String city;
  String province;
  String address;
  String email;
  String phone;
  String postalCode;

  UserModel({
    required this.username,
    required this.password,
    required this.firstName,
    required this.lastName,
    required this.city,
    required this.province,
    required this.address,
    required this.email,
    required this.phone,
    required this.postalCode,
  });
}
